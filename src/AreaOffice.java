
public class AreaOffice extends Distributor{
	protected String area_office_no;
	protected String area_office_name;
	protected String are_office_address;
	protected double total_sales_in_area;
	public String getArea_office_no() {
		return area_office_no;
	}
	public void setArea_office_no(String area_office_no) {
		this.area_office_no = area_office_no;
	}
	public String getArea_office_name() {
		return area_office_name;
	}
	public void setArea_office_name(String area_office_name) {
		this.area_office_name = area_office_name;
	}
	public String getAre_office_address() {
		return are_office_address;
	}
	public void setAre_office_address(String are_office_address) {
		this.are_office_address = are_office_address;
	}
	public double getTotal_sales_in_area() {
		return total_sales_in_area;
	}
	public void setTotal_sales_in_area(double total_sales_in_area) {
		this.total_sales_in_area = total_sales_in_area;
	}
}
